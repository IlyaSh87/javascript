let a = parseInt(prompt('введите число от 0 до 15'));


switch (a){
	case 0:
	alert('Вы ввели ноль');
	break;

	case 1:
	alert('Вы ввели один');
	break;

	case 2:
	alert('Вы ввели два');
	break;

	case 3:
	alert('Вы ввели три');
	break;

	case 4:
	alert('Вы ввели четыре');
	break;

	case 5:
	alert('Вы ввели пять');
	break;

	case 6:
	alert('Вы ввели шесть');
	break;

	case 7:
	alert('Вы ввели семь');
	break;

	case 8:
	alert('Вы ввели восемь');
	break;

	case 9:
	alert('Вы ввели девять');
	break;

	case 10:
	alert('Вы ввели десять');
	break;

	case 11:
	alert('Вы ввели одинадцать');
	break;

	case 12:
	alert('Вы ввели двенадцать');
	break;

	case 13:
	alert('Вы ввели тринадцать');
	break;

	case 14:
	alert('Вы ввели четырнадцать');
	break;

	case 15:
	alert('Вы ввели пятнадцать');
	break;

	default:
		alert('Вы ввели число не  входящее в диапозон 0 до 15');
}



